/* USER CODE BEGIN 4 */
void setMotorSpeed(int16_t speed){
	if(speed > 0){
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, speed);
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, 0);
	}else{
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 0);
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, -speed);
	}
}

void initAccelerometer(void){
	uint8_t buffer[1];
	buffer[0] = 0x40;
	HAL_I2C_Mem_Write(&hi2c2, 0xD4, 0x10, I2C_MEMADD_SIZE_8BIT, buffer, 1, 1000);
}

int16_t readAccel(uint8_t axxis){
	int16_t accel;
	uint8_t buffer[2];
	HAL_I2C_Mem_Read(&hi2c2, 0xD4, 0x28+(2*axxis), I2C_MEMADD_SIZE_8BIT, buffer, 2, 1000);
	accel = ((int16_t) (buffer[1]<<8) | buffer[0])*0.061;
	return accel;
}

void setServoPos(float grados){
	uint16_t timer = ((grados*2000)/180)+500;
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, timer);
}

